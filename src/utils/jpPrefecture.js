/**
 * 都道府県明記 or 市名先頭から推測（例: 大阪市… → 大阪府）
 * @param {string} address1
 */
export function inferPrefectureFromAddress1(address1) {
  const t0 = extractPrefectureFromAddress1(address1);
  if (t0) return t0;
  const t = String(address1 || '').trim().normalize('NFKC');
  if (!t) return '';
  for (const [city, pref] of CITY_TO_PREF) {
    if (t.startsWith(city)) return pref;
  }
  return '';
}
